    private void queryButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_queryButtonActionPerformed
        if (!queryArea.getText().equals("")) {
            String result = "";
            
            if (!rdfText.getText().equals("") && rdfText.isEnabled()) 
                result = SPARQLQuery.executeLocalQuery(rdfText.getText(), queryArea.getText(), sfe);
            else if (!urlText.getText().equals("") && urlText.isEnabled())
                result = SPARQLQuery.executeRemoteQuery(urlText.getText(), queryArea.getText(), sfe);
            else {
                consoleTextArea.setText("[!] Error: Introduce an URL/path to query");
                return;
            }
        
            consoleTextArea.setText(result);
        } else {
            consoleTextArea.setText("[!] Error: Insert a query to execute");
        }
    }//GEN-LAST:event_queryButtonActionPerformed

