    private String chooseSavePath() {
        JFileChooser fc = new JFileChooser();
        fc.setFileSelectionMode(JFileChooser.FILES_ONLY);
        fc.setCurrentDirectory(new java.io.File("."));
        
        fc.addChoosableFileFilter(new FileNameExtensionFilter("Text File", "txt"));
        fc.addChoosableFileFilter(new FileNameExtensionFilter("SPARQL Query Results XML File", "srx"));
        
        String f = "";
        int val = fc.showSaveDialog(null);
        
        if (val == JFileChooser.APPROVE_OPTION) f = fc.getSelectedFile().getAbsolutePath();
    
        if (txtFileRadio.isSelected()) {
            consoleTextArea.setText("[+] SPARQL result saved in " + txtFileRadio.getText().replace("File", "format"));
            if (!f.contains(".txt")) f += ".txt";
        } else if (srxFileRadio.isSelected()) {
            consoleTextArea.setText("[+] SPARQL result saved in " + srxFileRadio.getText().replace("File", "format"));
            if (!f.contains(".srx")) f += ".srx";
        }
        
        System.out.println(f);
        return f;            
    }
    
    private void loadFile(JTextField text) {
        JFileChooser fc = new JFileChooser();
        fc.setFileSelectionMode(JFileChooser.FILES_ONLY);
        fc.setCurrentDirectory(new java.io.File("."));
        
        fc.addChoosableFileFilter(new FileNameExtensionFilter("XML/RDF File", "rdf"));
        fc.addChoosableFileFilter(new FileNameExtensionFilter("TURTLE File", "ttl"));
        
        String f = null;
        int val = fc.showOpenDialog(null);
        
        if (val == JFileChooser.APPROVE_OPTION) f = fc.getSelectedFile().getAbsolutePath();
        
        text.setText(f);
        
        if (f == null)
            consoleTextArea.setText("[!] RDF file could not be loaded!");
        else
            consoleTextArea.setText("[+] RDF file loaded : " 
                    + f);
        
        consoleTextArea.append("\n\n");
    }

