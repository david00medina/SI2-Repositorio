package com.concept.cliente;

public class InfoEconomica {
	private boolean impagosPrevios;
	private float costeMax;
	
	public boolean isImpagosPrevios() {
		return impagosPrevios;
	}
	
	public void setImpagosPrevios(boolean impagosPrevios) {
		this.impagosPrevios = impagosPrevios;
	}
	
	public float getCosteMax() {
		return costeMax;
	}
	
	public void setCosteMax(float costeMax) {
		this.costeMax = costeMax;
	}
}
